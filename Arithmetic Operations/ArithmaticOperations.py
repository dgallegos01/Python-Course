# Looking at the math side of Python
print(10 + 3) # add
print(10 - 3) # subtract
print(10 * 3) # multiply
print(10 / 3) # divide
print(10 // 3) # also divide but outputs an integer
print(10 % 3) # modular or mod, returns the remainder of the division
print(10 ** 3) # exponent
# augmented assignment operater:
x = 10
x = x + 3 # this will add origninal x with 3 and saved as new x
x += 3 # this is called the augmented assignment operater, a shoter version of x = x + 3. work with any operation
print(x) 


