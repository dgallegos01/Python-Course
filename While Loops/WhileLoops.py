# Learning about while loops
i = 1 # i stands for index
while i <= 5: # this statement will remain true until i = 5 and return false
    print(i)
    i = i + 1 # this is used so prevent infinite i's
print("done")

# example 2
i = 1 
while i <= 5: 
    print('*' * i) # this will multiply the character in the string by i
    i = i + 1 
print("done")