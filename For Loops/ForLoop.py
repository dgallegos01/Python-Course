for item in 'Python': # This will print each character of the string one at a time
    print(item)

for item in ['Darrius', 'John', 'Sarah']: # this will print each word one at a time
    print(item)

for item in [1, 2, 3, 4]: # this will print each number one at a time
    print(item)

for item in range(10): # this will print each number from 1 to 9 (not including 10)
    print(item)

for item in range(5, 10, 2): # this will print numbers starting with 5, goes 2 steps forward and ends in range of 10
    print(item) # output will be 5 7 9


